<h1 style="color:#45A049; font-weight:lighter;font-size: 17px;">Bienvenue sur le site du Département Informatique DRSI</h1>
<p> L'offre de formation du département informatique a pour objectif de :
   - préparer les auditeurs et les étudiants à maîtriser les évolutions technologiques,
   - permettre d'acquérir le niveau d'expertise nécessaire à l'exercice des métiers informatiques,
   - faciliter l'insertion professionnelle et/ou l'évolution de carrière. 

Ce site présente un panorama des enseignements dispensés au sein du département informatique.
Le DRSI prépare à des diplômes nationaux (LMD) et à des diplômes d'établissement. Le diplôme d'ingénieur est reconnu par la Commission des Titres d'Ingénieur (CTI).
Les diplomes certifiés sont inscrits au registre national des certifications professionnelles.
Les certificats de compétence sont attribués par Le DRSI sur la base d'un ensemble d'enseignements focalisés sur un métier.
Toutes ces formations sont construites sur un socle commun d'unités d'enseignement.
Il existe de nombreuses passerelles possibles entre ces formations.
N'hésitez pas à contacter les responsables des différents diplômes.
Participez au forum et contribuez ainsi au partage d'information.
